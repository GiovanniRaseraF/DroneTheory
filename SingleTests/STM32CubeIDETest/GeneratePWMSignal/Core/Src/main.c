void gyro_calibration(ADC_HandleTypeDef *hadcz, uint16_t *outZOffset)
{
    uint16_t REPETITION = 2000;
    *outZOffset = 0;
    uint32_t sum = 0;

    for (int i = 0; i < REPETITION; i++) {
    	HAL_ADC_Start(hadcz);
    	HAL_ADC_PollForConversion(hadcz, HAL_MAX_DELAY);
        sum += HAL_ADC_GetValue(hadcz);
    }

    *outZOffset = sum / REPETITION;

    printf("gyroZOffset: %d\n", *outZOffset);
}
